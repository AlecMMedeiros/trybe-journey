//Write a program that sets three numbers into
//constants and returns true if at least one of
//the three is odd. Otherwise, it returns false.
//Bonus: Use just one "if".
const num1 = 2;
const num2 = 2;
const num3 = 1;
//Using one If 
if (num1 % 2 == 1 || num2 % 2 == 1 || num3 % 2 == 1) {
    console.log(true)
} else {
    console.log(false)
}
